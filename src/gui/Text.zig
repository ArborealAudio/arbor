        for (string) |c| {
            const glyph = getGlyph(c);
            const glyph_line = glyph[y * BMP_WIDTH .. y * BMP_WIDTH + GLYPH_WIDTH];
            for (0..GLYPH_WIDTH) |x| {
                if (glyph_line[x] == 1) print("*", .{}) else print(" ", .{});
            }
