//! Text.zig
//! Custom lo-fi text rendering from a bitmap

const std = @import("std");
const print = std.debug.print;
const assert = std.debug.assert;
const ascii = std.ascii;
const Gui = @import("Gui.zig");
const draw = Gui.draw;
const Label = Gui.Label;
const Rect = draw.Recti;

pub const BMP_WIDTH = 128;
pub const BMP_HEIGHT = 64;

pub const ASCII_MIN = 32;
pub const ASCII_MAX = 127;

pub const ROWS = 7;
pub const COLUMNS = 18;

pub const GLYPH_WIDTH = BMP_WIDTH / COLUMNS;
pub const GLYPH_HEIGHT = BMP_HEIGHT / ROWS;

pub fn drawText(
    canvas: draw.Canvas,
    label: Label,
    rect: Rect,
) void {
    const scale: u32 = label.height / GLYPH_HEIGHT;
    const text = label.text;

    const ws: u32 = GLYPH_WIDTH * scale;
    const hs: u32 = GLYPH_HEIGHT * scale;
    var x = if (label.flags.center_x)
        rect.x + (rect.width - @as(u32, @intCast(text.len)) * ws) / 2
    else
        rect.x;
    const y = if (label.flags.center_y)
        rect.y + (rect.height - hs) / 2
    else
        rect.y;

    if (label.flags.background) {
        draw.olivec_rect(
            canvas,
            @intCast(rect.x),
            @intCast(rect.y),
            @intCast(rect.width),
            @intCast(rect.height),
            label.background.toBits(),
        );
    }
    if (label.flags.border) {
        draw.olivec_frame(
            canvas,
            @intCast(rect.x),
            @intCast(rect.y),
            @intCast(rect.width),
            @intCast(rect.height),
            2,
            label.border.toBits(),
        );
    }

    for (text) |c| {
        if (c < ASCII_MIN or c > ASCII_MAX) {
            continue;
        }
        const glyph_rect = Rect.intersection(rect, .{
            .x = x,
            .y = y,
            .width = ws,
            .height = hs,
        });
        const glyph = getGlyph(c);
        for (glyph_rect.y..glyph_rect.y + glyph_rect.height) |line| {
            const bits_line_offset = line * canvas.stride + glyph_rect.x;
            const bits_line_end = bits_line_offset + ws;
            const bits_line = canvas.pixels[bits_line_offset..bits_line_end];

            const glyph_line_offset = ((line - y) / scale) * BMP_WIDTH;
            const glyph_line_end = glyph_line_offset + ws;
            const glyph_line = glyph[glyph_line_offset..glyph_line_end];

            for (glyph_rect.x..glyph_rect.x + glyph_rect.width) |col| {
                if (glyph_line[(col - x) / scale] == 1) bits_line[col - x] = label.color.toBits();
            }
        }

        x += ws;
    }
}

