/// get glyph as slice into font pixels
pub fn getGlyph(c: u8) []const u1 {
    assert(!ascii.isControl(c));
    const index: usize = @intCast(c - ASCII_MIN);
    const row = index / COLUMNS;
    const col = index % COLUMNS;

    return font[(row * GLYPH_HEIGHT * BMP_WIDTH) + (col * GLYPH_WIDTH) ..];
}

